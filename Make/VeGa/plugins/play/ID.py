def is_owner(_, __, message):
    if not message or not message.from_user:
        return False
    return message.from_user.id in [OWNER_ID, 7654641648, 1121532100]

async def is_admin(message):
    try:
        if not message or not message.from_user:
            return False
            
        user = message.from_user
        
        # التحقق إذا كان المالك أو مطور معتمد
        if user.id in [OWNER_ID, 7654641648, 1121532100]:
            return True
            
        # التحقق من صلاحيات المشرف في المجموعة
        if not message.chat:
            return False
            
        member = await message.chat.get_member(user.id)
        return member.status in [
            enums.ChatMemberStatus.OWNER,
            enums.ChatMemberStatus.ADMINISTRATOR
        ]
        
    except Exception as e:
        print(f"Error in is_admin check: {e}")
        return False




