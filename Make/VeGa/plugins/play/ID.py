@app.on_message(filters.command(["المالك", "صاحب الخرابه", "المنشي"], ""), group=95)
async def ownner(client: Client, message: Message):
    try:
        owner = None
        async for member in client.get_chat_members(
            message.chat.id, 
            filter=enums.ChatMembersFilter.ADMINISTRATORS
        ):
            if member.status == ChatMemberStatus.OWNER:
                owner = member.user
                break

        if not owner:
            return await message.reply_text("❌ لم يتم العثور على مالك للمجموعة")
        username = f"@{owner.username}" if owner.username else "لا يوجد"
        chat_title = message.chat.title or "مجموعة بدون اسم"
        
        caption = (
            f"<blockquote><b>"
            f"╭◉  ɴᴧᴍᴇ : {owner.first_name}\n"
            f"┃᚜◉ ᴜsᴇꝛ : {username}\n"
            f"╰◉  ɪᴅ : <code>{owner.id}</code>\n"
            f"╭◉  ᴄʜᴧᴛ : {chat_title}\n"
            f"╰◉  ɪᴅ.ᴄʜᴧᴛ : <code>{message.chat.id}</code>"
            f"</b></blockquote>"
        )

        # زر المالك
        button = InlineKeyboardMarkup([
            [InlineKeyboardButton(owner.first_name, url=f"https://t.me/{owner.username}")] if owner.username else
            [InlineKeyboardButton(owner.first_name, callback_data="no_username")]
        ])

        # إرسال الصورة أو النص
        if owner.photo:
            photos = [photo async for photo in client.get_chat_photos(owner.id, limit=1)]
            if photos:
                await message.reply_photo(
                    photo=photos[0].file_id,
                    caption=caption,
                    reply_markup=button,
                    has_spoiler=True  # تشويش الصورة
                )
                return
        
        # إذا لم توجد صورة
        await message.reply_text(
            caption,
            reply_markup=button
        )

    except Exception as e:
        print(f"Error in owner command: {e}")
        await message.reply_text("❌ حدث خطأ أثناء جلب معلومات المالك")



                          





