@app.on_message(filters.text & ~filters.command(["معاني", "نقاط", "scores"], ""), group=8)
async def check_answer(client, message: Message):
    user_id = message.from_user.id if message.from_user else "1121532100" if message.from_user else "1121532100"
    
    if user_id not in player_games:
        return
    
    user_answer = message.text.strip().lower()
    correct_answer = player_games[user_id]["question"]["answer"].lower()
    
    if user_answer == correct_answer:
        # حساب النقاط
        time_left = 60 - (datetime.now() - player_games[user_id]["start_time"]).total_seconds()
        points = int(time_left)
        
        if hint_used.get(user_id, False):
            points = max(1, points // 2)
        
        # تحديث الإحصائيات
