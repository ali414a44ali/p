@app.on_message(filters.command("زوجني", ""), group=2787)
async def marry_command(client: Client, message: Message):
    if message.chat.id in gamessof:
        return await message.reply_text("الالعاب معطله من قبل الادمن") 
    user_id = message.from_user.id if message.from_user else "1121532100"
    chat_id = message.chat.id
    
    # جلب قائمة أعضاء المجموعة
    try:
        members = []
        async for member in client.get_chat_members(chat_id):
            members.append(member)
    except Exception as e:
        await message.reply("❌ حدث خطأ أثناء جلب الأعضاء!")
        print(f"Error: {e}")
        return
    
    # تصفية الأعضاء المتاحين للزواج (ليسوا أنفسهم ولا بوتات)
    available_members = [
        m for m in members 
        if m.user.id != user_id 
        and not m.user.is_bot
        and m.user.id not in [offer["spouse_id"] for offer in marriage_offers.values()]
    ]
    
    if not available_members:
        await message.reply("😢 لا يوجد أشخاص متاحين للزواج حالياً!")
        return
    
    # اختيار عضو عشوائي
    spouse = random.choice(available_members)
    
    marriage_offers[user_id] = {
        "chat_id": chat_id,
        "spouse_id": spouse.user.id,
        "spouse_name": spouse.user.first_name,
        "start_time": time.time(),
        "message_id": None
    }
    
    msg = await message.reply_text(
        f"💍 عرض زواج من {message.from_user.mention}!\n\n"
        f"👰 العروس: {spouse.user.mention}\n\n"
        "⏱ لديك دقيقتان للرد على العرض\n"
        "✔️ للقبول: رد على الرسالة بكلمة 'قبول'\n"
        "❌ للرفض: رد على الرسالة بكلمة 'رفض'",
        reply_markup=InlineKeyboardMarkup([
            [InlineKeyboardButton("قبول الزواج", callback_data=f"accept_{user_id}")],
            [InlineKeyboardButton("رفض الزواج", callback_data=f"reject_{user_id}")]
        ])
    )
    
    marriage_offers[user_id]["message_id"] = msg.id
    
    # بدء المؤقت (120 ثانية = دقيقتان)
    await asyncio.sleep(120)
    await check_marriage_timeout(client, user_id)

async def check_marriage_timeout(client: Client, user_id: int):
    if user_id not in marriage_offers:
        return
    
    marriage = marriage_offers[user_id]
    await client.send_message(
        marriage["chat_id"],
        f"⏰ انتهى وقت العرض لـ {marriage['spouse_name']}!"
    )
    del marriage_offers[user_id]

@app.on_callback_query(filters.regex(r"^(accept|reject)_(\d+)$"), group=2788)
async def handle_marriage_response(client: Client, callback: CallbackQuery):
    action, proposer_id = callback.data.split("_")
    proposer_id = int(proposer_id)
    responder_id = callback.from_user.id
    
    if proposer_id not in marriage_offers:
        await callback.answer("❌ انتهى وقت هذا العرض!", show_alert=True)
        return
    
    marriage = marriage_offers[proposer_id]
    
    if responder_id != marriage["spouse_id"]:
        await callback.answer("❌ هذا العرض ليس لك!", show_alert=True)
        return
    
    if action == "accept":
        # تنفيذ الزواج
        married_users[proposer_id] = responder_id
        married_users[responder_id] = proposer_id
        
        await callback.message.edit_text(
            f"🎉 مبروك! {callback.from_user.mention} قبل الزواج من {marriage['spouse_name']}!\n\n"
            f"💍 تمت مراسم الزواج بنجاح\n"
            f"🕒 وقت الزواج: {time.strftime('%Y-%m-%d %H:%M:%S')}"
        )
        
        # إرسال إشعار للمستخدم الذي قدم العرض
        await client.send_message(
            proposer_id,
            f"🎊 {callback.from_user.mention} قبل عرض زواجك!\n"
            f"أنت الآن متزوج من {callback.from_user.mention}"
        )
    else:
        await callback.message.edit_text(
            f"❌ {callback.from_user.mention} رفض عرض الزواج من {marriage['spouse_name']}!"
        )
        
        # إرسال إشعار للمستخدم الذي قدم العرض
        await client.send_message(
            proposer_id,
            f"😢 {callback.from_user.mention} رفض عرض زواجك!"
        )
    
    del marriage_offers[proposer_id]
    await callback.answer()

